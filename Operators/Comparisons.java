class Comparisons {
    public static void main(String[] args) {
    	
        // 5 is smaller than 7
        boolean first = 5 < 7;
        
        // 9 is greater or equal to 2
        boolean second = 9 >= 2;
        
        System.out.println("5 is smaller than 7: " + first);
        System.out.println("9 is greater or equal to 2: " + second);
    }
}